    private fun uniquePathsWithObstacles(obstacleGrid: Array<IntArray>): Int {
        val m = obstacleGrid.size
        val n = obstacleGrid[0].size

        if (obstacleGrid[0][0] == 1) {
            return 0
        } else {
            obstacleGrid[0][0] = 1
        }

        for (i in 1 until m) {
            if (obstacleGrid[i - 1][0] != 0 && obstacleGrid[i][0] == 0) {
                obstacleGrid[i][0] = 1
            } else {
                obstacleGrid[i][0] = 0
            }
        }

        for (j in 1 until n) {
            if (obstacleGrid[0][j - 1] != 0 && obstacleGrid[0][j] == 0) {
                obstacleGrid[0][j] = 1
            } else {
                obstacleGrid[0][j] = 0
            }
        }

        for (i in 1 until m) {
            for (j in 1 until n) {
                if (obstacleGrid[i][j] == 1) {
                    obstacleGrid[i][j] = 0
                } else {
                    obstacleGrid[i][j] = obstacleGrid[i - 1][j] + obstacleGrid[i][j - 1]
                }
            }
        }

        return obstacleGrid[m - 1][n - 1]
    }
